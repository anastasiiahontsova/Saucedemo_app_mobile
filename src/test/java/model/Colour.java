package model;

public class Colour {

    String blue;
    String red;

    String black;
    String gray;

    public void colour() {
        this.blue = "blue";
        this.red = "red";
        this.black = "black";
        this.gray = "gray";
    }

    public String getBlue() {
        return blue;
    }

    public void setBlue(String blue) {
        this.blue = blue;
    }

    public String getRed() {
        return red;
    }

    public void setRed(String red) {
        this.red = red;
    }

    public String getBlack() {
        return black;
    }

    public void setBlack(String black) {
        this.black = black;
    }

    public String getGray() {
        return gray;
    }

    public void setGray(String gray) {
        this.gray = gray;
    }






}
